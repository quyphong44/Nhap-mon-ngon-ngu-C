#include <stdio.h>

int main (void) {
 // khai bao bien num kieu int
 int num;
 // khoi tao gia tri bien num
 num = 5;
 // hien thi bien num ra man hinh
 printf ("bien num co gia tri: %d",num);
 // khai bao bien x kieu char
 char c;
 // khoi tao gia tri cho bien
 c = 'a';
 // hien thi c ra man hinh
 printf("bien c co gia tri: %c", c);
 // hien thi dong: bien num = 5 va bien c = 4
  printf("bien num = %d va bien c = \n" , c);
 getchar();
 return 0;
}
 
